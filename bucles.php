<?php

/*
-------------------------------------------------------------
                           FOREACH

            PHP_EOL -> ES PARA DARLE UN ESPACIO
-------------------------------------------------------------
*/

$personas = ["luis", "pepito", "carlos","Martinez"];
$a = 1;

foreach ($personas as $persona){
echo $persona . "<br>";
}
 
/*
-------------------------------------------------------------
                           FOR
-------------------------------------------------------------
*/

for ($i=1; $i <= 10; $i++) { 
    echo $i. PHP_EOL;
}

echo "<br>";

for ($i = 0, $total = sizeof($personas); $i < $total; $i++){
    echo $personas[$i]. "<br>";
}

echo "<br>";

/*
-------------------------------------------------------------
                           DO WHILE
-------------------------------------------------------------
*/

do {
    echo $a . PHP_EOL;
    $a++;
} while ($a <= 20);

echo "<br>";




/*
-------------------------------------------------------------
                           WHILE
-------------------------------------------------------------
*/

$i= 1;

while ($i <= 30) {
